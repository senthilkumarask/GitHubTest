package com.bbb.commerce.order;

public class TBSOrderPropertyManager extends BBBOrderPropertyManager {

	/**
	 * mOnlineOrderNumberProperty
	 */
	private String mOnlineOrderNumberProperty;
	/**
	 * mOrderStateProperty
	 */
	private String mOrderStateProperty;
	/**
	 * mAddressProperty
	 */
	private String mAddressProperty;
	/**
	 * mEmailProperty
	 */
	private String mEmailProperty;
	/**
	 * mBillingAddressProperty
	 */
	private String mBillingAddressProperty;

	/**
	 * @return the onlineOrderNumberProperty
	 */
	public String getOnlineOrderNumberProperty() {
		return mOnlineOrderNumberProperty;
	}
	/**
	 * @param pOnlineOrderNumberProperty the onlineOrderNumberProperty to set
	 */
	public void setOnlineOrderNumberProperty(String pOnlineOrderNumberProperty) {
		mOnlineOrderNumberProperty = pOnlineOrderNumberProperty;
	}
	/**
	 * @return the orderStateProperty
	 */
	public String getOrderStateProperty() {
		return mOrderStateProperty;
	}
	/**
	 * @param pOrderStateProperty the orderStateProperty to set
	 */
	public void setOrderStateProperty(String pOrderStateProperty) {
		mOrderStateProperty = pOrderStateProperty;
	}
	/**
	 * @return the addressProperty
	 */
	public String getAddressProperty() {
		return mAddressProperty;
	}
	/**
	 * @param pAddressProperty the addressProperty to set
	 */
	public void setAddressProperty(String pAddressProperty) {
		mAddressProperty = pAddressProperty;
	}
	/**
	 * @return the emailProperty
	 */
	public String getEmailProperty() {
		return mEmailProperty;
	}
	/**
	 * @param pEmailProperty the emailProperty to set
	 */
	public void setEmailProperty(String pEmailProperty) {
		mEmailProperty = pEmailProperty;
	}
	/**
	 * @return the billingAddressProperty
	 */
	public String getBillingAddressProperty() {
		return mBillingAddressProperty;
	}
	/**
	 * @param pBillingAddressProperty the billingAddressProperty to set
	 */
	public void setBillingAddressProperty(String pBillingAddressProperty) {
		mBillingAddressProperty = pBillingAddressProperty;
	}
	
}
